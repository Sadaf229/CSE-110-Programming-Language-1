import java.util.Scanner;
public class Task_10{
    public static void main(String[]args){
        Scanner k= new Scanner(System.in);
        int a[]=new int[10];
        for(int i=0;i<a.length;i+=1){
            System.out.print("Enter a number");
            a[i]= k.nextInt();
            for(int f=0;f<=i;f+=1){
                if(f==a.length-1){
                    System.out.print(a[f]+"."); 
                    break;
                }
                System.out.print(a[f]+", ");
            }
            System.out.println();
        }
    }
}