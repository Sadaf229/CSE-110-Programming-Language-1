import java.util.Scanner;
public class Task_16{
    public static void main(String[]args){
        Scanner k= new Scanner(System.in);
        System.out.println("Enter Row Number");
        int r= k.nextInt();
        for(int rc=1;rc<=r;rc+=1){
            if(rc==1){
            System.out.print("*");
            }
            else if(rc==r){
                for(int c=1;c<=r;c+=1){
                    System.out.print("*");
                }
            }
            else{
                System.out.print("*");
                for(int sc=1;sc<rc-1;sc+=1){
                    System.out.print(" ");
                }
                System.out.print("*");
            }
            System.out.println();
        }
    }
}